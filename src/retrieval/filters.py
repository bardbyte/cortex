"""Filter resolution for the Cortex retrieval pipeline.

Takes raw extracted filters (field_hint + user values) and resolves them to
LookML-compatible filter expressions ready for Looker query generation.

Resolution strategy (5-pass, stops at first match):
  Pass 1: Exact match against known values (view-namespaced → bare fallback)
  Pass 2: Synonym expansion (SYNONYM_MAP → value map)
  Pass 3: Fuzzy match (Levenshtein distance ≤ 2, min input length 3)
  Pass 4: Embedding similarity (TODO — requires embed_fn from pipeline)
  Pass 5: Pass through with low confidence (flag for user review)

Additional handlers (before the 5-pass cascade):
  - Yesno dimensions: "yes"/"enrolled"/"active" → "Yes"
  - Negation: "not Gold" → operator "-", value "GOLD"
  - Numeric ranges: "between 1000 and 5000" → "[1000,5000]", "> 10000" → ">10000"
  - Time normalization: "Q4 2025" → "2025-10-01 to 2025-12-31"

Namespace architecture (multi-BU safe):
  Value maps are stored with "view_name.dimension_name" composite keys internally
  (_VALUE_MAP_NS) to prevent collisions when multiple BUs define dimensions with
  the same name but different valid values.

  At resolution time, the explore context determines which views are in scope.
  _find_view_for_dim() locates the owning view → namespaced lookup is preferred.
  Falls back to bare-name FILTER_VALUE_MAP (flattened, merged across views).

  Example: card_prod_id exists in both cmdl_card_main and custins, with the same
  values. If BU2 adds a different card_prod_id, the namespaced keys prevent collision.

Auto-derivation (Phase 1 — implemented):
  At import time, loads config/filter_catalog.json (generated by
  `python -m scripts.load_lookml_to_pgvector --mode catalog`).
  Auto-derived values from LookML CASE statements, descriptions, and
  always_filter declarations are merged with hardcoded fallbacks.

  Phase 2 (planned): BQ SELECT DISTINCT for raw-column dims (8/11 gaps).
  Phase 3 (planned): Learning loop from Pass 5 passthrough logs.

Integration point:
  Called from pipeline.py Step 4, AFTER explore selection.
  Resolved filters are attached to PipelineResult alongside scored explores.
"""

