# ─── NAMESPACE UTILITIES ─────────────────────────────────────────────

def _flatten_ns_map(ns_map: dict[str, dict[str, str]]) -> dict[str, dict[str, str]]:
    """Flatten view.dim keyed map to bare dim keys, merging across views.

    If two views define the same dimension name, their values are merged
    (both sets of user-term aliases are available via bare lookup).
    """
    flat: dict[str, dict[str, str]] = {}
    for ns_key, values in ns_map.items():
        dim = ns_key.split(".", 1)[1] if "." in ns_key else ns_key
        if dim in flat:
            flat[dim].update(values)
        else:
            flat[dim] = dict(values)
    return flat


def _find_view_for_dim(
    dimension: str,
    explore_name: str,
    explore_views: dict[str, list[str]],
    ns_map: dict[str, dict[str, str]],
) -> str:
    """Find which view in an explore owns a dimension, using the namespaced map.

    Returns the view name if exactly one view in the explore has this dimension.
    Returns "" if ambiguous or unknown (falls back to bare lookup).
    """
    if not explore_name or explore_name not in explore_views:
        return ""
    views_in_explore = explore_views[explore_name]
    matches = [v for v in views_in_explore if f"{v}.{dimension}" in ns_map]
    return matches[0] if len(matches) == 1 else ""


# ─── MERGE: AUTO-DERIVED + HARDCODED ─────────────────────────────────

def _merge_catalogs() -> tuple[
    dict[str, dict[str, str]],  # namespaced value map (view.dim keys)
    dict[str, dict[str, str]],  # flattened value map (bare dim keys, backward compat)
    dict[str, dict[str, str]],  # synonym map
    set[str],                   # yesno dimensions
    dict[str, str],             # partition fields
    dict[str, list[str]],       # explore → views mapping
]:
    """Merge auto-derived catalog over hardcoded fallbacks.

    Value maps are stored view-namespaced (view.dim keys) internally.
    A flattened bare-name map is produced for backward compatibility.
    When view context is available, namespaced lookup is preferred.
    """
    catalog = _load_catalog()

    # ── Namespaced value map: hardcoded base + auto-derived overlay ──
    ns_map = dict(_HARDCODED_VALUE_MAP_NS)
    for ns_key, auto_vals in catalog.get("value_map", {}).items():
        if ns_key in ns_map:
            merged = dict(ns_map[ns_key])
            merged.update(auto_vals)
            ns_map[ns_key] = merged
        else:
            ns_map[ns_key] = dict(auto_vals)

    # ── Flatten to bare-name map for backward compat ──
    flat_map = _flatten_ns_map(ns_map)

    # ── Synonym map: hardcoded (value-level synonyms, bare-keyed) ──
    synonym_map = dict(_HARDCODED_SYNONYM_MAP)

    # ── Yesno dimensions: union ──
    yesno = set(catalog.get("yesno_dimensions", []))
    yesno.update({
        "is_replacement", "is_not_cm_initiated", "is_cm_initiated",
        "is_active_standard", "is_active_premium", "is_dining_at_restaurant",
        "is_cancelled", "has_authorized_users",
    })

    # ── Partition fields: auto-derived wins ──
    partition_fields = dict(_HARDCODED_PARTITION_FIELDS)
    partition_fields.update(catalog.get("partition_fields", {}))

    # ── Explore → views mapping ──
    explore_views = catalog.get("explore_views", {})

    return ns_map, flat_map, synonym_map, yesno, partition_fields, explore_views


