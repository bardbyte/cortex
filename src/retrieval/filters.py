# and written to config/filter_catalog.json.

_CATALOG_PATH = Path(__file__).resolve().parents[2] / "config" / "filter_catalog.json"


def _load_catalog() -> dict[str, Any]:
    """Load auto-derived filter catalog from JSON, or empty dict if missing."""
    if not _CATALOG_PATH.exists():
        logger.info("No filter catalog at %s — using hardcoded maps only", _CATALOG_PATH)
        return {}
    try:
        with open(_CATALOG_PATH) as f:
            catalog = json.load(f)
        logger.info(
            "Loaded filter catalog: %d value maps, %d synonym groups, "
            "%d yesno dims, %d partition fields",
            len(catalog.get("value_map", {})),
            len(catalog.get("synonyms", {})),
            len(catalog.get("yesno_dimensions", [])),
            len(catalog.get("partition_fields", {})),
        )
        return catalog
    except Exception as e:
        logger.warning("Failed to load filter catalog: %s — using hardcoded maps only", e)
        return {}


