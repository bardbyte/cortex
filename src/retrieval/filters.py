def resolve_filters(
    entities: list[dict[str, Any]],
    explore_name: str,
) -> FilterResolutionResult:
    """Resolve all filters from extracted entities for the selected explore.

    Uses explore context to resolve filter dimensions to the correct view,
    enabling namespaced value map lookups that prevent cross-BU collisions.

    Handles:
    1. Categorical/yesno filters → LookML values (5-pass resolution)
    2. Negation → Looker "-VALUE" syntax
    3. Numeric ranges → Looker ">N", "[N,M]" syntax
    4. Time range → normalized to Looker date syntax + correct partition field
    5. Mandatory partition filter → auto-injected if user didn't specify
    """
    result = FilterResolutionResult()

    for entity in entities:
        entity_type = entity.get("type", "")

        if entity_type == "filter":
            field_hint = entity.get("name", "")
            values = entity.get("values", [])
            operator = entity.get("operator", "=")

            if not field_hint or not values:
                continue

            # Resolve which view in this explore owns this dimension
            view_name = _find_view_for_dim(
                field_hint, explore_name, _EXPLORE_VIEWS, _VALUE_MAP_NS,
            )

            for user_value in values:
                resolved = resolve_single_value(field_hint, str(user_value), view_name)
                if resolved:
                    # Preserve operator from entity extraction if not overridden
                    if resolved.operator == "=" and operator != "=":
                        resolved.operator = operator
                    result.resolved_filters.append(resolved)
                else:
                    result.unresolved.append({
                        "field_hint": field_hint,
                        "value": user_value,
                        "operator": operator,
                    })

        elif entity_type == "time_range":
            time_values = entity.get("values", [])
            if time_values:
                partition_field = EXPLORE_PARTITION_FIELDS.get(
                    explore_name, "partition_date"
                )
                raw_time = str(time_values[0])
                normalized_time = normalize_time_expression(raw_time)
                result.resolved_filters.append(ResolvedFilter(
                    field_name=partition_field,
                    value=normalized_time,
                    operator="=",
                    resolution_pass=1,
                    original_value=raw_time,
                    confidence=1.0,
                ))
                logger.info(
                    "Time: %s → %s = %s (explore=%s)",
                    raw_time, partition_field, normalized_time, explore_name,
                )

    # ── Mandatory partition filter ──
    partition_field = EXPLORE_PARTITION_FIELDS.get(explore_name, "partition_date")
    user_specified_partition = any(
        f.field_name == partition_field for f in result.resolved_filters
    )

    if not user_specified_partition:
        result.mandatory_filters.append(ResolvedFilter(
            field_name=partition_field,
            value=DEFAULT_PARTITION_VALUE,
            operator="=",
            resolution_pass=0,
            original_value="(auto-injected)",
            confidence=1.0,
        ))
        logger.info(
            "Auto-injected: %s = %s (explore=%s)",
            partition_field, DEFAULT_PARTITION_VALUE, explore_name,
        )

    return result


def format_resolution_report(result: FilterResolutionResult) -> str:
    """Human-readable filter resolution report for debugging."""
    lines = ["Filter Resolution Report", "=" * 40]

    if result.resolved_filters:
        lines.append("\nResolved Filters:")
        for f in result.resolved_filters:
            confidence_bar = "#" * int(f.confidence * 10)
            neg_marker = " [NEGATED]" if f.is_negated else ""
            lines.append(
                f"  {f.field_name} {f.operator} '{f.value}'{neg_marker} "
                f"(pass {f.resolution_pass}, confidence: [{confidence_bar:<10}] {f.confidence:.0%})"
            )
            if f.original_value != f.value:
                lines.append(f"    ← user said: '{f.original_value}'")

    if result.mandatory_filters:
        lines.append("\nMandatory Filters (auto-injected):")
        for f in result.mandatory_filters:
            lines.append(f"  {f.field_name} = '{f.value}'")

    if result.unresolved:
        lines.append("\nUnresolved (need disambiguation):")
        for u in result.unresolved:
            lines.append(f"  {u['field_hint']} {u['operator']} '{u['value']}'")

    looker_filters = result.to_looker_filters()
    lines.append(f"\nLooker filter dict: {looker_filters}")

    return "\n".join(lines)
