# ─── DATA STRUCTURES ──────────────────────────────────────────────────

@dataclass
class ResolvedFilter:
    """A single resolved filter ready for Looker query generation."""
    field_name: str           # LookML dimension name (e.g., "generation")
    value: str                # Resolved LookML value (e.g., "Millennial")
    operator: str = "="       # Filter operator ("=", "!=", ">", "<", ">=", "<=", "between")
    resolution_pass: int = 0  # Which pass resolved this (1-5, 0=mandatory)
    original_value: str = ""  # What the user said
    confidence: float = 1.0   # Resolution confidence (1.0 for exact, lower for fuzzy/embedding)
    is_negated: bool = False  # True if user said "not X"


@dataclass
class FilterResolutionResult:
    """Complete filter resolution output for a query."""
    resolved_filters: list[ResolvedFilter] = field(default_factory=list)
    mandatory_filters: list[ResolvedFilter] = field(default_factory=list)
    unresolved: list[dict[str, Any]] = field(default_factory=list)

    def to_looker_filters(self) -> dict[str, str]:
        """Convert to flat dict for Looker MCP query generation.

        Multi-value filters on the same dimension are combined using Looker's
        comma-separated syntax: "GOLD,PLAT" for IN (GOLD, PLAT).
        Negated values use Looker's "-VALUE" prefix: "-GOLD" for NOT GOLD.
        Numeric filters use Looker syntax: ">10000", ">=5000", "[1000, 5000]".
        """
        filters: dict[str, str] = {}
        # Mandatory filters first (partition dates, etc.)
        for f in self.mandatory_filters:
            filters[f.field_name] = f.value
        # Then user-specified filters — accumulate multi-value with comma
        for f in self.resolved_filters:
            # Looker negation syntax: prefix value with "-"
            display_value = f"-{f.value}" if f.is_negated else f.value

            if f.field_name in filters:
                existing = filters[f.field_name]
                if display_value not in existing.split(","):
                    filters[f.field_name] = f"{existing},{display_value}"
            else:
                filters[f.field_name] = display_value
        return filters


# ─── TIME NORMALIZATION ────────────────────────────────────────────────
# Converts natural language time expressions to Looker date filter syntax.
# Looker accepts: "last N days/weeks/months/quarters/years",
#                  "YYYY-MM-DD to YYYY-MM-DD", "this quarter", etc.

# Quarter → month mapping
_QUARTER_MONTHS = {
    "1": (1, 3), "q1": (1, 3),
    "2": (4, 6), "q2": (4, 6),
    "3": (7, 9), "q3": (7, 9),
    "4": (10, 12), "q4": (10, 12),
}

# Patterns for time normalization
_TIME_PATTERNS: list[tuple[re.Pattern, str]] = [
    # "Q4 2025" or "Q4 of 2025" or "4th quarter 2025"
    (re.compile(r"(?:q|quarter\s*)(\d)\s*(?:of\s+)?(\d{4})", re.IGNORECASE), "quarter_year"),
    # "last N days/weeks/months/quarters/years"
    (re.compile(r"last\s+(\d+)\s+(day|week|month|quarter|year)s?", re.IGNORECASE), "relative"),
    # "this quarter/month/year"
    (re.compile(r"this\s+(day|week|month|quarter|year)", re.IGNORECASE), "this_period"),
    # "last quarter/month/year" (without N)
    (re.compile(r"last\s+(day|week|month|quarter|year)$", re.IGNORECASE), "last_period"),
    # "YYYY" alone (full year)
    (re.compile(r"^(\d{4})$"), "full_year"),
    # "YYYY-MM-DD" or "YYYY-MM-DD to YYYY-MM-DD" — already Looker-compatible
    (re.compile(r"\d{4}-\d{2}-\d{2}"), "already_formatted"),
]


def normalize_time_expression(raw: str) -> str:
    """Convert a natural language time expression to Looker date filter syntax.

    Examples:
        "Q4 2025"      → "2025-10-01 to 2025-12-31"
        "last quarter"  → "last 1 quarters"
        "last 6 months" → "last 6 months"  (already Looker-compatible)
        "this year"     → "this year"       (already Looker-compatible)
        "2025"          → "2025-01-01 to 2025-12-31"
    """
    raw_stripped = raw.strip()

    for pattern, pattern_type in _TIME_PATTERNS:
        match = pattern.search(raw_stripped)
        if not match:
            continue

        if pattern_type == "quarter_year":
            quarter = match.group(1).lower()
            year = match.group(2)
            months = _QUARTER_MONTHS.get(quarter) or _QUARTER_MONTHS.get(f"q{quarter}")
            if months:
                start_month, end_month = months
                # Last day of the end month
                if end_month == 12:
                    end_day = 31
                elif end_month in (3, 6, 9):
                    end_day = 30
                else:
                    end_day = 28  # Feb fallback
                return f"{year}-{start_month:02d}-01 to {year}-{end_month:02d}-{end_day:02d}"

        elif pattern_type == "relative":
            # "last 6 months" → "last 6 months" (Looker native)
            n = match.group(1)
            unit = match.group(2).lower()
            # Looker uses plural for N > 1
            unit_plural = f"{unit}s" if int(n) > 1 else f"{unit}s"
            return f"last {n} {unit_plural}"

        elif pattern_type == "this_period":
            period = match.group(1).lower()
            return f"this {period}"

        elif pattern_type == "last_period":
            period = match.group(1).lower()
            return f"last 1 {period}s"

        elif pattern_type == "full_year":
            year = match.group(1)
            return f"{year}-01-01 to {year}-12-31"

        elif pattern_type == "already_formatted":
            return raw_stripped

    # Unrecognized — pass through (Looker may still parse it)
    logger.debug("Time expression not normalized, passing through: %s", raw)
    return raw_stripped


# ─── NUMERIC FILTER HANDLING ──────────────────────────────────────────

# Patterns for numeric filter expressions
_NUMERIC_PATTERNS: list[tuple[re.Pattern, str]] = [
    # "between X and Y" or "from X to Y"
    (re.compile(r"(?:between|from)\s+([\d,.]+)\s+(?:and|to)\s+([\d,.]+)", re.IGNORECASE), "between"),
    # "> X", ">= X", "greater than X", "more than X", "above X", "over X"
    (re.compile(r"(?:>=?|greater\s+than|more\s+than|above|over|exceeds?)\s*([\d,.]+)", re.IGNORECASE), "gt"),
    # "< X", "<= X", "less than X", "under X", "below X"
    (re.compile(r"(?:<=?|less\s+than|under|below)\s*([\d,.]+)", re.IGNORECASE), "lt"),
    # "exactly X" or just a plain number
    (re.compile(r"(?:exactly\s+)?([\d,.]+)$", re.IGNORECASE), "exact"),
]


def resolve_numeric_filter(raw_value: str) -> ResolvedFilter | None:
    """Parse a numeric filter expression into Looker filter syntax.

    Looker numeric filter syntax:
        >10000     greater than
        >=10000    greater than or equal
        <5000      less than
        <=5000     less than or equal
        [1000,5000]  between (inclusive)
        10000      exactly

    Returns None if the input is not a numeric expression.
    """
    raw_stripped = raw_value.strip()

    for pattern, pattern_type in _NUMERIC_PATTERNS:
        match = pattern.search(raw_stripped)
        if not match:
            continue

        if pattern_type == "between":
            low = match.group(1).replace(",", "")
            high = match.group(2).replace(",", "")
            return ResolvedFilter(
                field_name="",  # Set by caller
                value=f"[{low},{high}]",
                operator="between",
                resolution_pass=1,
                original_value=raw_value,
                confidence=0.95,
            )

        elif pattern_type == "gt":
            num = match.group(1).replace(",", "")
            op = ">=" if ">=" in raw_stripped else ">"
            return ResolvedFilter(
                field_name="",
                value=f"{op}{num}",
                operator=op,
                resolution_pass=1,
                original_value=raw_value,
                confidence=0.95,
            )

        elif pattern_type == "lt":
            num = match.group(1).replace(",", "")
            op = "<=" if "<=" in raw_stripped else "<"
            return ResolvedFilter(
                field_name="",
                value=f"{op}{num}",
                operator=op,
                resolution_pass=1,
                original_value=raw_value,
                confidence=0.95,
            )

        elif pattern_type == "exact":
            num = match.group(1).replace(",", "")
            return ResolvedFilter(
                field_name="",
                value=num,
                operator="=",
                resolution_pass=1,
                original_value=raw_value,
                confidence=0.90,
            )

    return None


# ─── NEGATION DETECTION ───────────────────────────────────────────────

def _detect_negation(user_value: str) -> tuple[str, bool]:
    """Detect and strip negation prefixes from a filter value.

    Returns (stripped_value, is_negated).

    Examples:
        "not Gold"       → ("Gold", True)
        "excluding PLAT" → ("PLAT", True)
        "Gold"           → ("Gold", False)
        "non-revolving"  → ("revolving", True)
    """
    user_lower = user_value.lower().strip()
    for prefix in NEGATION_PREFIXES:
        if user_lower.startswith(prefix):
            stripped = user_value.strip()[len(prefix):].strip()
            if stripped:  # Don't return empty string
                return stripped, True
    return user_value, False


# ─── RESOLUTION ENGINE ─────────────────────────────────────────────────

def _levenshtein_distance(s1: str, s2: str) -> int:
    """Compute Levenshtein edit distance between two strings."""
    if len(s1) < len(s2):
        return _levenshtein_distance(s2, s1)
    if len(s2) == 0:
        return len(s1)

    prev_row = list(range(len(s2) + 1))
    for i, c1 in enumerate(s1):
        curr_row = [i + 1]
        for j, c2 in enumerate(s2):
            cost = 0 if c1 == c2 else 1
            curr_row.append(min(
                curr_row[j] + 1,        # insert
                prev_row[j + 1] + 1,    # delete
                prev_row[j] + cost,      # substitute
            ))
        prev_row = curr_row
    return prev_row[-1]


def _get_value_map_for_dim(dimension: str, view_name: str = "") -> dict[str, str]:
    """Get the value map for a dimension, preferring namespaced lookup.

    If view_name is known, look up view.dim in _VALUE_MAP_NS first.
    Always falls back to the bare-name FILTER_VALUE_MAP.
    """
    if view_name:
        ns_key = f"{view_name}.{dimension}"
        ns_vals = _VALUE_MAP_NS.get(ns_key)
        if ns_vals:
            return ns_vals
    return FILTER_VALUE_MAP.get(dimension, {})


def _resolve_value_pass1_exact(
    dimension: str,
    user_value: str,
    view_name: str = "",
) -> ResolvedFilter | None:
    """Pass 1: Exact match against value map (namespaced → bare fallback)."""
    user_lower = user_value.lower().strip()

    vals = _get_value_map_for_dim(dimension, view_name)
    mapped = vals.get(user_lower)
    if mapped:
        logger.debug("Pass 1 (exact): %s=%s → %s", dimension, user_value, mapped)
        return ResolvedFilter(
            field_name=dimension,
            value=mapped,
            resolution_pass=1,
            original_value=user_value,
            confidence=1.0,
        )
    return None


def _resolve_value_pass2_fuzzy(
    dimension: str,
    user_value: str,
    max_distance: int = 2,
    min_input_length: int = 3,
    view_name: str = "",
) -> ResolvedFilter | None:
    """Fuzzy match via Levenshtein distance ≤ max_distance.

    Safety guard: inputs shorter than min_input_length are skipped.
    Short strings have enormous Levenshtein neighborhoods relative to the
    dictionary — "y" is distance 1 from "no" and distance 2 from "yes",
    producing false positives.
    """
    user_lower = user_value.lower().strip()

    if len(user_lower) < min_input_length:
        return None

    vals = _get_value_map_for_dim(dimension, view_name)
    if not vals:
        return None

    best_match: str | None = None
    best_distance = max_distance + 1

    for known_key, known_value in vals.items():
        dist = _levenshtein_distance(user_lower, known_key)
        if dist <= max_distance and dist < best_distance:
            best_distance = dist
            best_match = known_value

    if best_match:
        confidence = 1.0 - (best_distance / max(len(user_lower), 1))
        logger.debug(
            "Fuzzy: %s=%s → %s (distance=%d, confidence=%.2f)",
            dimension, user_value, best_match, best_distance, confidence,
        )
        return ResolvedFilter(
            field_name=dimension,
            value=best_match,
            resolution_pass=3,
            original_value=user_value,
            confidence=max(confidence, 0.5),
        )
    return None


def _resolve_value_pass3_synonym(
    dimension: str,
    user_value: str,
) -> ResolvedFilter | None:
    """Synonym expansion via SYNONYM_MAP → then resolve through value map."""
    user_lower = user_value.lower().strip()

    if dimension not in SYNONYM_MAP:
        return None

    canonical_key = SYNONYM_MAP[dimension].get(user_lower)
    if canonical_key is None:
        return None

    if dimension in FILTER_VALUE_MAP:
        mapped = FILTER_VALUE_MAP[dimension].get(canonical_key)
        if mapped:
            logger.debug(
                "Synonym: %s=%s → '%s' → %s",
                dimension, user_value, canonical_key, mapped,
            )
            return ResolvedFilter(
                field_name=dimension,
                value=mapped,
                resolution_pass=2,
                original_value=user_value,
                confidence=0.85,
            )
    return None


def _resolve_yesno(
    dimension: str,
    user_value: str,
) -> ResolvedFilter | None:
    """Resolve yesno dimension values to Looker's "Yes"/"No" format."""
    if dimension not in YESNO_DIMENSIONS:
        return None

    user_lower = user_value.lower().strip()
    if user_lower in ("yes", "true", "y", "1", "enrolled", "active", "has"):
        return ResolvedFilter(
            field_name=dimension,
            value="Yes",
            resolution_pass=1,
            original_value=user_value,
            confidence=1.0,
        )
    elif user_lower in ("no", "false", "n", "0", "not enrolled", "inactive", "not"):
        return ResolvedFilter(
            field_name=dimension,
            value="No",
            resolution_pass=1,
            original_value=user_value,
            confidence=1.0,
        )
    return None


def resolve_single_value(
    dimension: str,
    user_value: str,
    view_name: str = "",
) -> ResolvedFilter | None:
    """Run resolution on a single (dimension, value) pair.

    Args:
        dimension: LookML dimension name (bare, e.g. "generation")
        user_value: Raw user input (e.g. "millennials", "not Gold")
        view_name: Optional view name for namespaced lookup. When provided,
            resolution prefers view-specific values over bare-name fallback.
            This prevents collisions when multiple BUs define same dimension name.

    Handles negation, numeric expressions, yesno, and the 5-pass cascade.
    """
    # ── Negation detection ──
    stripped_value, is_negated = _detect_negation(user_value)

    # ── Numeric filter check (before categorical resolution) ──
    numeric_result = resolve_numeric_filter(stripped_value)
    if numeric_result:
        numeric_result.field_name = dimension
        numeric_result.is_negated = is_negated
        return numeric_result

    # ── Yesno shortcut ──
    result = _resolve_yesno(dimension, stripped_value)
    if result:
        result.is_negated = is_negated
        return result

    # ── Pass 1: Exact match (namespaced → bare fallback) ──
    result = _resolve_value_pass1_exact(dimension, stripped_value, view_name)
    if result:
        result.is_negated = is_negated
        return result

    # ── Pass 2: Synonym expansion (before fuzzy — domain knowledge > edit distance) ──
    result = _resolve_value_pass3_synonym(dimension, stripped_value)
    if result:
        result.resolution_pass = 2
        result.is_negated = is_negated
        return result

    # ── Pass 3: Fuzzy match (Levenshtein ≤ 2, min length 3) ──
    result = _resolve_value_pass2_fuzzy(dimension, stripped_value, view_name=view_name)
    if result:
        result.resolution_pass = 3
        result.is_negated = is_negated
        return result

    # ── Pass 4: Embedding similarity (TODO) ──

    # ── Pass 5: Passthrough with low confidence ──
    logger.info(
        "All passes failed for %s=%s — passing through as-is",
        dimension, user_value,
    )
    return ResolvedFilter(
        field_name=dimension,
        value=stripped_value,
        resolution_pass=5,
        original_value=user_value,
        confidence=0.3,
        is_negated=is_negated,
    )


